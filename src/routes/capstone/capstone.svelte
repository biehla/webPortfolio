<h1 id="eventify"><strong>Eventify</strong></h1>
<p><br /></p>
<h2 id="project-summary"><strong>Project Summary</strong></h2>
<p>Eventify is a web-based platform designed to centralize event
listings from various sources, helping users discover and book events in
one place. The application aims to simplify event discovery by
eliminating the need to check multiple websites, while also supporting
local businesses through promotional tools for their events.</p>
<p><br /></p>
<h2 id="project-vision"><strong>Project Vision</strong></h2>
<p>Eventify’s vision is to create an all-in-one solution where users can
discover, plan, and book events anywhere in the world. The platform
strives to enhance social engagement by simplifying event discovery and
supporting local businesses in their promotional efforts.</p>
<p><br /></p>
<h2 id="projectbusiness-requirements"><strong>Project/Business
Requirements</strong></h2>
<ul>
<li>Centralized event aggregation and discovery features</li>
<li>User-friendly interface for browsing and booking events</li>
<li>Event search filters (location, date, category, etc.)</li>
<li>User login and profile creation</li>
<li>Secure authentication system with Lucia Auth and Argon2 hashing</li>
<li>Backend system that gathers event data from various
sources/APIs</li>
<li>Future promotional tools for businesses to list and market their
events</li>
</ul>
<p><br /></p>
<h2 id="project-plan"><strong>Project Plan</strong></h2>
<p>The project is planned to run from October 7, 2024, to March 28,
2025, with the following key milestones: - Requirements Finalization:
January 20, 2025 - Design Complete: February 10, 2025 - Finalizing
Documentation: March 25, 2025</p>
